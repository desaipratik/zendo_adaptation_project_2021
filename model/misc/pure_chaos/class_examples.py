class four_wheels():
    def __init__self(self):
        self.__init__self()

    '''function '''
    def drive(self, xpos=0, fuel = 10):
        while fuel > 0:
            fuel -= 1
            xpos +=1
        return xpos



class car(four_wheels):
    def __init__self(self):
        self.__init__self()


c = car()
print(c.drive())























